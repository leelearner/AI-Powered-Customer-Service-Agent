"""
RAGAS evaluation script for the RAG pipeline.

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
How RAGAS works
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

RAGAS (RAG Assessment) uses an LLM as a judge to automatically evaluate
RAG pipelines without requiring manual scoring at scale. Each sample
consists of four fields:

  user_input         -- the user's question
  retrieved_contexts -- list of context chunks returned by the retriever
  response           -- the answer generated by the RAG system
  reference          -- a human-written ground-truth answer

The four metrics each target a different component of the RAG pipeline:

  +----------------------------------------------------------+
  |  Metric               Evaluates    Needs reference?      |
  |  Faithfulness         generator    no                    |
  |  Answer Relevancy     generator    no                    |
  |  Context Precision    retriever    yes                   |
  |  Context Recall       retriever    yes                   |
  +----------------------------------------------------------+

See individual function docstrings for how each metric is computed.

Usage:
    python -m rag.ragas_eval
"""

